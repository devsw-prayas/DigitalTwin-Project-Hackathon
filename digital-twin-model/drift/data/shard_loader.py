def main():
    """Test the shard loader."""
    import argparse
    
    parser = argparse.ArgumentParser()
    parser.add_argument("--shard_dir", type=str, default="data_shards")
    parser.add_argument("--batch_size", type=int, default=8)
    args = parser.parse_args()
    
    print("Testing ShardLoader...")
    
    loader = ShardLoader(
        shard_dir=args.shard_dir,
        batch_size=args.batch_size,
        n_windows=3,
    )
    
    train_loader, val_loader = loader.get_loaders(phase=1)
    
    print(f"\nIterating through train loader...")
    for i, (X, y, routing, persona) in enumerate(train_loader):
        print(f"  Batch {i}: X={X.shape}, y={y.shape}, routing={routing.shape}")
        if i >= 2:
            break
    
    print("\nShardLoader test passed!")


if __name__ == "__main__":
    main()
